interface AgencySidebarProps {
  agencyName?: string;
  isVerified?: boolean;
}

export function AgencySidebar({ agencyName, isVerified }: AgencySidebarProps) {
  const pathname = usePathname();
  const [openGroups, setOpenGroups] = useState<string[]>(["communications", "operations", "financial", "insights"]);

  const isActive = (href: string) => {
    if (href === "/d/agency") {
      return pathname === "/d/agency";
    }
    return pathname.startsWith(href);
  };

  const isGroupActive = (items: NavItem[]) => {
    return items.some(item => isActive(item.href));
  };

  const toggleGroup = (groupId: string) => {
    setOpenGroups(prev => 
      prev.includes(groupId) 
        ? prev.filter(id => id !== groupId)
        : [...prev, groupId]
    );
  };

  return (
    <aside className="glass fixed left-0 top-16 z-40 hidden h-[calc(100vh-4rem)] w-64 flex-col border-r border-white/[0.06] lg:flex">
      {/* Agency Header */}
      <div className="border-b border-white/[0.06] p-4">
        <div className="flex items-center gap-3">
          <div className="flex h-10 w-10 items-center justify-center rounded-xl bg-gradient-to-br from-shield-500 to-shield-600 text-white shadow-glow-sm">
            <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 21V5a2 2 0 00-2-2H7a2 2 0 00-2 2v16m14 0h2m-2 0h-5m-9 0H3m2 0h5M9 7h1m-1 4h1m4-4h1m-1 4h1m-5 10v-5a1 1 0 011-1h2a1 1 0 011 1v5m-4 0h4" />
            </svg>
          </div>
          <div className="min-w-0 flex-1">
            <h2 className="truncate font-display text-sm font-semibold text-white">
              {agencyName || "Your Agency"}
            </h2>
            {isVerified && (
              <span className="inline-flex items-center gap-1 text-xs text-shield-400">
                <svg className="h-3 w-3" fill="currentColor" viewBox="0 0 20 20">
                  <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z" clipRule="evenodd" />
                </svg>
                Verified
              </span>
            )}
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 overflow-y-auto p-3">
        <ul className="space-y-1">
          {navSections.map((section, index) => {
            if (section.type === "item" && section.item) {
              const item = section.item;
              const active = isActive(item.href);
              return (
                <li key={item.href}>
                  <Link
                    href={item.href}
                    className={`relative flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium transition-all ${
                      active
                        ? "text-white"
                        : "text-zinc-400 hover:bg-white/[0.03] hover:text-white"
                    }`}
                  >
                    {active && (
                      <motion.div
                        layoutId="activeTab"
                        className="absolute inset-0 rounded-xl bg-shield-500/20"
                        transition={{ type: "spring", duration: 0.4, bounce: 0.2 }}
                      />
                    )}
                    <span className={`relative z-10 ${active ? "text-shield-400" : ""}`}>
                      {item.icon}
                    </span>
                    <span className="relative z-10">{item.label}</span>
                  </Link>
                </li>
              );
            }

            if (section.type === "group" && section.group) {
              const group = section.group;
              const isOpen = openGroups.includes(group.id);
              const hasActiveItem = isGroupActive(group.items);

              return (
                <li key={group.id}>
                  {/* Group Header */}
                  <button
                    onClick={() => toggleGroup(group.id)}
                    className={`relative flex w-full items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium transition-all ${
                      hasActiveItem
                        ? "text-white"
                        : "text-zinc-400 hover:bg-white/[0.03] hover:text-white"
                    }`}
                  >
                    <span className={`relative z-10 ${hasActiveItem ? "text-shield-400" : ""}`}>
                      {group.icon}
                    </span>
                    <span className="relative z-10 flex-1 text-left">{group.label}</span>
                    <motion.svg
                      className="relative z-10 h-4 w-4"
                      fill="none"
                      viewBox="0 0 24 24"
                      stroke="currentColor"
                      animate={{ rotate: isOpen ? 180 : 0 }}
                      transition={{ duration: 0.2 }}
                    >
                      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                    </motion.svg>
                  </button>

                  {/* Group Items */}
                  <AnimatePresence initial={false}>
                    {isOpen && (
                      <motion.ul
                        initial={{ height: 0, opacity: 0 }}
                        animate={{ height: "auto", opacity: 1 }}
                        exit={{ height: 0, opacity: 0 }}
                        transition={{ duration: 0.2 }}
                        className="overflow-hidden"
                      >
                        {group.items.map((item) => {
                          const active = isActive(item.href);
                          return (
                            <li key={item.href} className="mt-1">
                              <Link
                                href={item.href}
                                className={`relative flex items-center gap-3 rounded-xl py-2 pl-11 pr-3 text-sm transition-all ${
                                  active
                                    ? "text-white"
                                    : "text-zinc-500 hover:bg-white/[0.03] hover:text-zinc-300"
                                }`}
                              >
                                {active && (
                                  <motion.div
                                    layoutId="activeSubTab"
                                    className="absolute inset-0 rounded-xl bg-shield-500/10"
                                    transition={{ type: "spring", duration: 0.3, bounce: 0.2 }}
                                  />
                                )}
                                <span className={`relative z-10 ${active ? "text-shield-400" : ""}`}>
                                  {item.icon}
                                </span>
                                <span className="relative z-10">{item.label}</span>
                              </Link>
                            </li>
                          );
                        })}
                      </motion.ul>
                    )}
                  </AnimatePresence>
                </li>
              );
            }

            return null;
          })}
        </ul>
      </nav>

      {/* Quick Actions Footer */}
      <div className="border-t border-white/[0.06] p-3">
        <Link
          href="/d/agency/staff/add"
          className="flex w-full items-center justify-center gap-2 rounded-xl bg-shield-500 px-4 py-2.5 text-sm font-medium text-white shadow-glow-sm transition hover:bg-shield-400"
        >
          <svg className="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 4v16m8-8H4" />
          </svg>
          Add Staff
        </Link>
      </div>
    </aside>
  );
}

// Mobile navigation component
export function AgencyMobileNav() {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === "/d/agency") {
      return pathname === "/d/agency";
    }
    return pathname.startsWith(href);
  };

  // Extract top-level items for mobile nav
  const mobileNavItems = navSections
    .filter(s => s.type === "item" && s.item)
    .map(s => s.item!)
    .slice(0, 5);

  return (
    <nav className="glass fixed bottom-0 left-0 right-0 z-50 border-t border-white/[0.06] lg:hidden">
      <div className="flex items-center justify-around px-2 py-2">
        {mobileNavItems.map((item) => {
          const active = isActive(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center gap-1 rounded-lg px-3 py-2 text-xs transition ${
                active ? "text-shield-400" : "text-zinc-500 hover:text-zinc-300"
              }`}
            >
              {item.icon}
              <span className="truncate max-w-[60px]">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
